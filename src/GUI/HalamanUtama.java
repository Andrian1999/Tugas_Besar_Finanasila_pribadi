    private void btn_save_kActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_save_kActionPerformed
       pengeluaran = Integer.parseInt(txt_jmlh_k.getText());
       kat_k = txt_kat_k.getText();
       ket_k = txt_ket_k.getText();
       tgl = cmb_tgl.getSelectedItem().toString();
       bln = cmb_bln.getSelectedItem().toString(); 
       thn  = cmb_th.getSelectedItem().toString();
       String tanggal = thn+"-"+bln+"-"+tgl;
    
       try {    
           sql = "INSERT INTO pengeluaran(`Tanggal`,`pengeluaran`,`keterangan`,`kategori`) "
                   + "VALUES(?,?,?,?)";
           p = con.prepareStatement(sql);
           p.setString(1,tanggal);
           p.setInt(2, pengeluaran);
           p.setString(3,ket_k);
           p.setString(4,kat_k);
            p.executeUpdate();
            p.close(); 
             JOptionPane.showMessageDialog(null, "Sukses" );
            
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "gagal "+e);
        }       
    }//GEN-LAST:event_btn_save_kActionPerformed

