    void load()
    {
        try {
            
            sql = "SELECT * FROM pemasukan";
            rs = st.executeQuery(sql);
            
            while(rs.next())
            {
                Object [] obj = new Object[4];
                obj [0] = rs.getString("tgl");
                obj [1] = rs.getInt("pemasukan");
                obj [2] = rs.getString("kategori");
                obj [3] = rs.getString("keterangan");
                model_m.addRow(obj);
            }
        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "gagal" + e);
        }
    }
